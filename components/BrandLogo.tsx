export function platformKey(p: string): keyof typeof PATHS {
  const n = (p || "").toLowerCase();
  if (n.includes("tiktok") || n === "tt") return "tiktok";
  if (n.includes("short") || n.includes("youtube") || n === "yt") return "youtube";
  if (n.includes("reel") || n.includes("insta") || n === "ig") return "instagram";
  if (n.includes("twitch")) return "twitch";
  if (n.includes("linkedin")) return "linkedin";
  return "youtube";
}

export function BrandLogo({ name, style, className }: { name: string; style?: CSSProperties; className?: string }) {
  const k = platformKey(name);
  return (
    <svg viewBox="0 0 24 24" aria-hidden="true" className={className} style={style}>
      <path fill="currentColor" d={PATHS[k]} />
    </svg>
  );
}

/* pastille de marque : fond coloré + logo blanc (le placeholder remplacé) */
export function PlatformTile({ p, size = 26, style }: { p: string; size?: number; style?: CSSProperties }) {
  const k = platformKey(p);
  return (
    <span
      aria-hidden="true"
      style={{
        width: size, height: size, flex: "none", borderRadius: Math.round(size * 0.28),
        background: BG[k], display: "inline-flex", alignItems: "center", justifyContent: "center", ...style,
      }}
    >
      <BrandLogo name={k} style={{ width: Math.round(size * 0.6), height: Math.round(size * 0.6), color: "#fff" }} />
    </span>
  );
}

